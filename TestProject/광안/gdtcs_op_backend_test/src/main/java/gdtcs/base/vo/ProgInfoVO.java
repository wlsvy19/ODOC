package gdtcs.base.vo;

public class ProgInfoVO {
    private String icCode;
    private String prgCode;
    private String highPrgCode;
    private String prgNm;
    private String prgNote;

    public String getIcCode() {
        return icCode;
    }
    public void setIcCode(String icCode) {
        this.icCode = icCode;
    }
    public String getPrgCode() {
        return prgCode;
    }
    public void setPrgCode(String prgCode) {
        this.prgCode = prgCode;
    }
    public String getHighPrgCode() {
        return highPrgCode;
    }
    public void setHighPrgCode(String highPrgCode) {
        this.highPrgCode = highPrgCode;
    }
    public String getPrgNm() {
        return prgNm;
    }
    public void setPrgNm(String prgNm) {
        this.prgNm = prgNm;
    }
    public String getPrgNote() {
        return prgNote;
    }
    public void setPrgNote(String prgNote) {
        this.prgNote = prgNote;
    }
}
