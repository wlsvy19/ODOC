package variable;

public class Var5 {
	public static void main(String[] args) {
		// 1. 변수 선언, 초기화 각각 따로 하기
		int a;
		a = 1;
		System.out.println("a = " + a);

		// 2. 변수 선언과 초기화를 한번에 하기
		int b = 2;
		System.out.println("b = " + b);

		// 3. 여러 번수 선언과 초기화를 한번에 하기
		int c = 3, d= 4;
		System.out.println("c = " + c);
		System.out.println("d = " + d);

		
	}
}
