package variable;

// 변수 선언과 초기화
public class Var2 {

	public static void main(String[] args) {
		int var1; // 변수 선언
		var1 = 20; // 변수 초기화

		System.out.println(10);
		System.out.println(10);
		System.out.println(10);

		System.out.println(var1);
		System.out.println(var1);
		System.out.println(var1);
	}
}
